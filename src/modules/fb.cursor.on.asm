* fb.cursor.on
* Turn cursor on
***************************************************************
* bl @fb.cursor.on
*--------------------------------------------------------------
* INPUT
* none
*--------------------------------------------------------------
* OUTPUT
* none
*--------------------------------------------------------------
* Register usage
* none
*--------------------------------------------------------------
* Notes
********|*****|*********************|**************************
fb.cursor.on:
        dect  stack
        mov   r11,*stack            ; Save return address
        ;-------------------------------------------------------
        ; Turn cursor on
        ;-------------------------------------------------------
        clr   @fb.curtoggle         ; Turn cursor on        
        ;-------------------------------------------------------
        ; Exit
        ;-------------------------------------------------------
fb.cursor.on.exit:
        mov   *stack+,r11           ; Pop r11
        b     *r11                  ; Return         